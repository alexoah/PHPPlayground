/**
 *	from PHP Switch: Exercise 1 ( https://www.w3schools.com/php/exercise.asp?filename=exercise_switch1 )
 *
 *	question:
 *	Create a switch statement that will output "Hello" if $color is "red", and "welcome" if $color is "green".
 *
 *	switch ($color) {
 *		____ "red":
 *			echo "Hello";
 *			break;
 *		____ "green":
 *			echo "Welcome";
 *			break;
 *	}
 *
 */
switch ($color) {
	case "red":
		echo "Hello";
		break;
	case "green":
		echo "Welcome";
		break;
}